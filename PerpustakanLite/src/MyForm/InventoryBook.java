    private void TableInventoryMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableInventoryMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = new DefaultTableModel();
        model = (DefaultTableModel) TableInventory.getModel();
        int index = TableInventory.getSelectedRow();
        
        kodeBuku.setText(model.getValueAt(index, 0).toString());
        judulBuku.setText(model.getValueAt(index, 1).toString());
        pengarang.setText(model.getValueAt(index, 2).toString());
        penerbit.setText(model.getValueAt(index, 3).toString());
        for (int i = 0; i < Status.getItemCount(); i++){
            if (model.getValueAt(index, 4).toString() == Status.getItemAt(i)){
                Status.setSelectedIndex(i);
            }
        }
    }//GEN-LAST:event_TableInventoryMouseClicked

