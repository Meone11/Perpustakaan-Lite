    private void AddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtonActionPerformed
        // TODO add your handling code here:
        
        if (validateText()){
            
            String kode = kodeBuku.getText().trim();
            String judul = judulBuku.getText().trim();
            String Pengarang = pengarang.getText().trim();
            String Penerbit = penerbit.getText().trim();
            String status = Status.getItemAt(Status.getSelectedIndex());

            a.InsertUpdateDelete('i', kode, judul, Pengarang, Penerbit, status);
        }
        
        kodeBuku.setText("");
        judulBuku.setText("");
        pengarang.setText("");
        penerbit.setText("");
        Status.setSelectedIndex(0);       
        
    }//GEN-LAST:event_AddButtonActionPerformed

