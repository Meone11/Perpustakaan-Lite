    public boolean validateText(){
        if (kodeBuku.getText().trim().isEmpty() || judulBuku.getText().trim().isEmpty() || pengarang.getText().trim().isEmpty() 
                || penerbit.getText().trim().isEmpty() || Status.getItemAt(Status.getSelectedIndex()).equals("-")){
            JOptionPane.showMessageDialog(null, "Tolong isi Semua Data!!", "Failed", 2);
            return false;
        }else{
            return true;
        }
    }
    
    public void fillTable(){
        
        //add to table
        a.FillToTableInventory(TableInventory);
        
        //Set VAlue to Center
        for (int i = 0; i < TableInventory.getColumnCount(); i++){
            TableInventory.getColumnModel().getColumn(i).setCellRenderer(center);
        }
        
        //set Tbale Header to Center
        JTableHeader header = TableInventory.getTableHeader();
        ((DefaultTableCellRenderer)header.getDefaultRenderer()).setHorizontalAlignment(SwingConstants.CENTER);
        
        //Set Column Width
        TableInventory.getColumnModel().getColumn(0).setPreferredWidth(70); //Kolom Kode Buku
        TableInventory.getColumnModel().getColumn(1).setPreferredWidth(200); // Kolom Judul Buku
        TableInventory.getColumnModel().getColumn(2).setPreferredWidth(150); // Kolon Pengarang
        TableInventory.getColumnModel().getColumn(3).setPreferredWidth(150); // Kolom Penerbit
        TableInventory.getColumnModel().getColumn(4).setPreferredWidth(70); // Kolom Status
    }

