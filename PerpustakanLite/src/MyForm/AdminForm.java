    private void reportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reportActionPerformed
        TableCalender.setModel(new DefaultTableModel(null, new Object[]{"Kode Buku", "Judul Buku", "Pengarang", "Penerbit", "Status"}));
        getDataPeminjaman();
        try {
            // TODO add your handling code here:
            String path = "src/MyForm/Laporan.jrxml";
                        
            JRBeanCollectionDataSource dataSource = new JRBeanCollectionDataSource(list);
            File file = new File(path);
            JasperDesign jDesign = JRXmlLoader.load(file);
            
            
            JasperReport jReport = JasperCompileManager.compileReport(jDesign);
            JasperPrint jPrint = JasperFillManager.fillReport(jReport, null, dataSource);
            
            JasperViewer.viewReport(jPrint, false);
            
            
        } catch (JRException ex) {
            Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_reportActionPerformed

