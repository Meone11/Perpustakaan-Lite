    private void getDataPeminjaman(){
        DefaultTableModel model = new DefaultTableModel();
        model = (DefaultTableModel) TableCalender.getModel();
        try{
            MyClass.PeminjamanSQL sql = new MyClass.PeminjamanSQLImpl();
            list = sql.getDataPeminjaman();
            
            for (Peminjaman obj : list){
                model.addRow(new Object[]{obj.getKode(), obj.getJudul(), obj.getNama(), obj.getTdate(), obj.getPdate()});
            }
            
        }catch(SQLException e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Gagal Mengambil Data!!");
        }
    }
    
    public void fillTable(String date){
        
        //add to table
        a.FillToTableRiwayat(TableCalender, date);
        
        //Set VAlue to Center
        for (int i = 0; i < TableCalender.getColumnCount(); i++){
            TableCalender.getColumnModel().getColumn(i).setCellRenderer(center);
        }
        
        //set Tbale Header to Center
        JTableHeader header = TableCalender.getTableHeader();
        ((DefaultTableCellRenderer)header.getDefaultRenderer()).setHorizontalAlignment(SwingConstants.CENTER);
        
        //Set Column Width
        TableCalender.getColumnModel().getColumn(0).setPreferredWidth(70); //Kolom Kode Buku
        TableCalender.getColumnModel().getColumn(1).setPreferredWidth(200); // Kolom Judul Buku
        TableCalender.getColumnModel().getColumn(2).setPreferredWidth(150); // Kolon Pengarang
        TableCalender.getColumnModel().getColumn(3).setPreferredWidth(100); // Kolom Penerbit
        TableCalender.getColumnModel().getColumn(4).setPreferredWidth(100); // Kolom Status
    }

